package Polymorphism;

public class Class_Maruti extends Class_Car {

	@Override
	public void milage() {
		System.out.println("Maruti Milage");
	}
	@Override
	public void speed() {
		System.out.println("Maruti Speed");
	}
	@Override
	public void color() {
		System.out.println("Maruti Color");
	}
	@Override
	public void EngineType() {
		System.out.println("Maruti Engine");
	}
	@Override
	public void fuelType() {
		System.out.println("Maruti Fuel");
	}

}
