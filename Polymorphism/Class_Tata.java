package Polymorphism;

public class Class_Tata extends Class_Car {

	@Override
	public void milage() {
		System.out.println("Tata Milage");
	}
	@Override
	public void speed() {
		System.out.println("Tata Speed");
	}
	@Override
	public void color() {
		System.out.println("Tata Color");
	}
	@Override
	public void EngineType() {
		System.out.println("Tata Engine");
	}
	@Override
	public void fuelType() {
		System.out.println("Tata Fuel");
	}

}
