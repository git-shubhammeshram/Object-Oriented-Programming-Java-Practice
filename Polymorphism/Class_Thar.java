package Polymorphism;

public class Class_Thar extends Class_Car {
	
	@Override
	public void milage() {
		System.out.println("Thar Milage");
	}
	@Override
	public void speed() {
		System.out.println("Thar Speed");
	}
	@Override
	public void color() {
		System.out.println("Thar Color");
	}
	@Override
	public void EngineType() {
		System.out.println("Thar Engine");
	}
	@Override
	public void fuelType() {
		System.out.println("Thar Fuel");
	}


}
